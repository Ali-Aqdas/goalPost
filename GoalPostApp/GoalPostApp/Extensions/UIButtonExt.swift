import UIKit

extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4922404289, green: 0.7722371817, blue: 0.4631441236, alpha: 1)
    }
    
    func deSlectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6999676824, green: 0.8635095954, blue: 0.6780783534, alpha: 1)
    }
}

